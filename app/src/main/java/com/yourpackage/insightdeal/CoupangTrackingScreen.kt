                    onClick = { showAddDialog = true },
                    containerColor = Color.White,
                    contentColor = PrimaryOrange,
                    modifier = Modifier.size(56.dp)
                ) {
                    Icon(Icons.Default.Add, contentDescription = "ÏÉÅÌíà Ï∂îÍ∞Ä")
                }
            }
        }
        
        // üîÑ Î°úÎî© ÏÉÅÌÉú
        if (isLoading) {
