                    CircularProgressIndicator(color = PrimaryOrange)
                    Spacer(modifier = Modifier.height(16.dp))
                    Text("ÏÉÅÌíà Ï†ïÎ≥¥ Í∞ÄÏ†∏Ïò§Îäî Ï§ë...")
                }
            }
        } else if (products.isEmpty()) {
            // üè∑Ô∏è Îπà ÏÉÅÌÉú ÌôîÎ©¥ (Ìè¥ÏÑºÌä∏ Ïä§ÌÉÄÏùº)
