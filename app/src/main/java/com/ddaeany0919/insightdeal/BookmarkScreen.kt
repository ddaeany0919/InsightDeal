@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun BookmarkScreen(
    onDealClick: (DealItem) -> Unit,
    onBackClick: () -> Unit
) {
    val context = LocalContext.current
    val bookmarkManager = remember { BookmarkManager.getInstance(context) }
    
    val bookmarks by bookmarkManager.bookmarks.collectAsState()
    val tags by bookmarkManager.tags.collectAsState()
    val stats by bookmarkManager.bookmarkStats.collectAsState()
    
    var selectedTags by remember { mutableStateOf<Set<String>>(emptySet()) }
    var sortBy by remember { mutableStateOf(BookmarkSortBy.DATE_DESC) }
    var showStats by remember { mutableStateOf(false) }
    
    // ÌïÑÌÑ∞ÎßÅÎêú Î∂ÅÎßàÌÅ¨ Î™©Î°ù
    val filteredBookmarks = remember(bookmarks, selectedTags, sortBy) {
        bookmarkManager.searchBookmarks(
            tags = selectedTags,
            sortBy = sortBy
        )
    }
    
    Scaffold(
        topBar = {
            TopAppBar(
                title = {
                    Text(
                        text = "üîñ Ï¶êÍ≤®Ï∞æÍ∏∞",
                        fontWeight = FontWeight.Bold
                    )
                },
                navigationIcon = {
                    IconButton(onClick = onBackClick) {
                        Icon(Icons.Default.ArrowBack, contentDescription = "Îí§Î°úÍ∞ÄÍ∏∞")
                    }
                },
                actions = {
                    IconButton(
                        onClick = { showStats = !showStats }
                    ) {
                        Icon(
                            Icons.Default.Analytics,
                            contentDescription = "ÌÜµÍ≥Ñ",
                            tint = if (showStats) MaterialTheme.colorScheme.primary 
                                  else MaterialTheme.colorScheme.onSurface
                        )
                    }
                    
                    // Ï†ïÎ†¨ Î©îÎâ¥
                    var showSortMenu by remember { mutableStateOf(false) }
                    
                    IconButton(onClick = { showSortMenu = true }) {
                        Icon(Icons.Default.Sort, contentDescription = "Ï†ïÎ†¨")
                    }
                    
                    DropdownMenu(
                        expanded = showSortMenu,
                        onDismissRequest = { showSortMenu = false }
                    ) {
                        SortOption.values().forEach { option ->
                            DropdownMenuItem(
                                text = { Text(option.displayName) },
                                onClick = {
                                    sortBy = option.sortBy
                                    showSortMenu = false
                                },
                                leadingIcon = {
                                    if (sortBy == option.sortBy) {
                                        Icon(
                                            Icons.Default.Check,
                                            contentDescription = null,
                                            tint = MaterialTheme.colorScheme.primary
                                        )
                                    }
                                }
                            )
                        }
                    }
                }
            )
        }
    ) { paddingValues ->
        LazyColumn(
            modifier = Modifier
                .fillMaxSize()
                .padding(paddingValues),
            verticalArrangement = Arrangement.spacedBy(16.dp),
            contentPadding = PaddingValues(16.dp)
        ) {
            // üìä Î∂ÅÎßàÌÅ¨ ÌÜµÍ≥Ñ (ÌÜ†Í∏Ä Í∞ÄÎä•)
            if (showStats) {
                item {
                    BookmarkStatsCard(stats = stats)
                }
            }
            
            // üè∑Ô∏è ÌÉúÍ∑∏ ÌïÑÌÑ∞
            item {
                TagFilterSection(
                    tags = tags,
                    selectedTags = selectedTags,
                    onTagToggle = { tag ->
                        selectedTags = if (selectedTags.contains(tag)) {
                            selectedTags - tag
                        } else {
                            selectedTags + tag
                        }
                    },
                    onClearTags = { selectedTags = emptySet() }
                )
            }
            
            // üìã Î∂ÅÎßàÌÅ¨ Î™©Î°ù
            if (filteredBookmarks.isEmpty()) {
                item {
                    BookmarkEmptyState(
                        hasFilters = selectedTags.isNotEmpty(),
                        onClearFilters = { selectedTags = emptySet() }
                    )
                }
            } else {
                item {
                    Text(
                        text = "üìå Ï†ÄÏû•Îêú Ìï´Îîú ${filteredBookmarks.size}Í∞ú",
                        fontSize = 16.sp,
                        fontWeight = FontWeight.Medium,
                        modifier = Modifier.padding(bottom = 8.dp)
                    )
                }
                
                items(filteredBookmarks) { bookmark ->
                    BookmarkItemCard(
                        bookmark = bookmark,
                        onClick = {
                            // DealItemÏúºÎ°ú Î≥ÄÌôòÌï¥ÏÑú ÌÅ¥Î¶≠ Ï≤òÎ¶¨
                            val dealItem = DealItem(
                                id = bookmark.dealId,
                                title = bookmark.title,
                                price = bookmark.currentPrice,
                                imageUrl = bookmark.imageUrl,
                                url = bookmark.url,
                                siteName = bookmark.siteName
                            )
                            onDealClick(dealItem)
                        },
                        onRemove = {
                            bookmarkManager.removeBookmark(bookmark.id)
                        },
                        onTagsUpdate = { newTags ->
                            bookmarkManager.updateBookmarkTags(bookmark.id, newTags)
                        }
                    )
                }
            }
        }
    }
}

