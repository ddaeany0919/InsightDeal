@Composable
private fun TagFilterSection(
    tags: List<BookmarkTag>,
    selectedTags: Set<String>,
    onTagToggle: (String) -> Unit,
    onClearTags: () -> Unit
) {
    Column {
        Row(
            modifier = Modifier.fillMaxWidth(),
            horizontalArrangement = Arrangement.SpaceBetween,
            verticalAlignment = Alignment.CenterVertically
        ) {
            Text(
                text = "üè∑Ô∏è ÌÉúÍ∑∏ ÌïÑÌÑ∞",
                fontSize = 16.sp,
                fontWeight = FontWeight.Medium
            )
            
            if (selectedTags.isNotEmpty()) {
                TextButton(onClick = onClearTags) {
                    Text("Ï†ÑÏ≤¥ Ìï¥Ï†ú")
                }
            }
        }
        
        Spacer(modifier = Modifier.height(8.dp))
        
        LazyRow(
            horizontalArrangement = Arrangement.spacedBy(8.dp)
        ) {
            items(tags.filter { it.usageCount > 0 }.sortedByDescending { it.usageCount }) { tag ->
                FilterChip(
                    onClick = { onTagToggle(tag.name) },
                    label = {
                        Text("${tag.name} (${tag.usageCount})")
                    },
                    selected = selectedTags.contains(tag.name),
                    colors = FilterChipDefaults.filterChipColors(
                        selectedContainerColor = Color(android.graphics.Color.parseColor(tag.color)),
                        selectedLabelColor = Color.White
                    )
                )
            }
        }
    }
}

@Composable
private fun BookmarkItemCard(
    bookmark: BookmarkItem,
    onClick: () -> Unit,
    onRemove: () -> Unit,
    onTagsUpdate: (Set<String>) -> Unit
) {
    var showDeleteDialog by remember { mutableStateOf(false) }
    
    Card(
        modifier = Modifier
            .fillMaxWidth()
            .clickable { onClick() },
        elevation = CardDefaults.cardElevation(defaultElevation = 4.dp)
    ) {
        Column(
            modifier = Modifier.padding(16.dp)
        ) {
            Row(
                modifier = Modifier.fillMaxWidth(),
                verticalAlignment = Alignment.Top
            ) {
                // ÏÉÅÌíà Ïù¥ÎØ∏ÏßÄ
                AsyncImage(
                    model = bookmark.imageUrl,
                    contentDescription = bookmark.title,
                    modifier = Modifier
                        .size(80.dp)
                        .clip(RoundedCornerShape(8.dp))
                        .background(MaterialTheme.colorScheme.surfaceVariant)
                )
                
                Spacer(modifier = Modifier.width(12.dp))
                
                // ÏÉÅÌíà Ï†ïÎ≥¥
                Column(
                    modifier = Modifier.weight(1f)
                ) {
                    // ÏÇ¨Ïù¥Ìä∏Î™Ö + Ï†ÄÏû• ÎÇ†Ïßú
                    Row(
                        modifier = Modifier.fillMaxWidth(),
                        horizontalArrangement = Arrangement.SpaceBetween
                    ) {
                        Text(
                            text = "[${bookmark.siteName}]",
                            fontSize = 12.sp,
                            color = MaterialTheme.colorScheme.primary,
                            fontWeight = FontWeight.Medium
                        )
                        
                        Text(
                            text = SimpleDateFormat("MM/dd", Locale.getDefault()).format(Date(bookmark.createdAt)),
                            fontSize = 12.sp,
                            color = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                    }
                    
                    Spacer(modifier = Modifier.height(4.dp))
                    
                    // ÏÉÅÌíàÎ™Ö
                    Text(
                        text = bookmark.title,
                        fontSize = 14.sp,
                        fontWeight = FontWeight.Medium,
                        maxLines = 2,
                        overflow = TextOverflow.Ellipsis
                    )
                    
                    Spacer(modifier = Modifier.height(8.dp))
                    
                    // Í∞ÄÍ≤© Ï†ïÎ≥¥
                    Row(
                        verticalAlignment = Alignment.CenterVertically
                    ) {
                        Text(
                            text = "${NumberFormat.getInstance().format(bookmark.currentPrice)}Ïõê",
                            fontSize = 16.sp,
                            fontWeight = FontWeight.Bold,
                            color = MaterialTheme.colorScheme.primary
                        )
                        
                        // Í∞ÄÍ≤© Î≥ÄÎèô ÌëúÏãú
                        if (bookmark.currentPrice != bookmark.originalPrice) {
                            Spacer(modifier = Modifier.width(8.dp))
                            
                            val priceDiff = bookmark.originalPrice - bookmark.currentPrice
                            val isDown = priceDiff > 0
                            
                            Surface(
                                shape = RoundedCornerShape(12.dp),
                                color = if (isDown) Color.Red else Color.Blue
                            ) {
                                Text(
                                    text = "${if (isDown) "-" else "+"}${NumberFormat.getInstance().format(kotlin.math.abs(priceDiff))}Ïõê",
                                    fontSize = 11.sp,
                                    color = Color.White,
                                    modifier = Modifier.padding(horizontal = 6.dp, vertical = 2.dp)
                                )
                            }
                        }
                    }
                }
                
                // ÏÇ≠Ï†ú Î≤ÑÌäº
                IconButton(
                    onClick = { showDeleteDialog = true },
                    modifier = Modifier.size(24.dp)
                ) {
                    Icon(
                        Icons.Default.Delete,
                        contentDescription = "ÏÇ≠Ï†ú",
                        modifier = Modifier.size(18.dp),
                        tint = MaterialTheme.colorScheme.error
                    )
                }
            }
            
            // ÌÉúÍ∑∏Îì§
            if (bookmark.tags.isNotEmpty()) {
                Spacer(modifier = Modifier.height(8.dp))
                
                LazyRow(
                    horizontalArrangement = Arrangement.spacedBy(4.dp)
                ) {
                    items(bookmark.tags.toList()) { tag ->
                        AssistChip(
                            onClick = { },
                            label = {
                                Text(
                                    text = tag,
                                    fontSize = 11.sp
                                )
                            },
                            modifier = Modifier.height(24.dp)
                        )
                    }
                }
            }
        }
    }
    
    // ÏÇ≠Ï†ú ÌôïÏù∏ Îã§Ïù¥ÏñºÎ°úÍ∑∏
    if (showDeleteDialog) {
        AlertDialog(
            onDismissRequest = { showDeleteDialog = false },
            title = { Text("Î∂ÅÎßàÌÅ¨ ÏÇ≠Ï†ú") },
            text = { Text("Ï†ïÎßêÎ°ú Ïù¥ Î∂ÅÎßàÌÅ¨Î•º ÏÇ≠Ï†úÌïòÏãúÍ≤†ÏäµÎãàÍπå?") },
            confirmButton = {
                TextButton(
                    onClick = {
                        onRemove()
                        showDeleteDialog = false
                    }
                ) {
                    Text("ÏÇ≠Ï†ú", color = MaterialTheme.colorScheme.error)
                }
            },
            dismissButton = {
                TextButton(onClick = { showDeleteDialog = false }) {
                    Text("Ï∑®ÏÜå")
                }
            }
        )
    }
}

