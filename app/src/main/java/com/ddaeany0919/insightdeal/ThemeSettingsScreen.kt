@Composable
private fun ColorSchemeSection(
    currentScheme: AppColorScheme,
    onSchemeChange: (AppColorScheme) -> Unit
) {
    Card(
        modifier = Modifier.fillMaxWidth(),
        elevation = CardDefaults.cardElevation(defaultElevation = 4.dp)
    ) {
        Column(
            modifier = Modifier.padding(20.dp)
        ) {
            Row(
                verticalAlignment = Alignment.CenterVertically,
                modifier = Modifier.padding(bottom = 16.dp)
            ) {
                Icon(
                    Icons.Default.Palette,
                    contentDescription = null,
                    tint = MaterialTheme.colorScheme.primary,
                    modifier = Modifier.size(24.dp)
                )
                
                Spacer(modifier = Modifier.width(8.dp))
                
                Text(
                    text = "üé® Ïª¥Îü¨ ÌÖåÎßà",
                    fontSize = 18.sp,
                    fontWeight = FontWeight.Bold
                )
            }
            
            LazyRow(
                horizontalArrangement = Arrangement.spacedBy(16.dp)
            ) {
                items(AppColorScheme.values()) { scheme ->
                    ColorSchemeOption(
                        scheme = scheme,
                        isSelected = scheme == currentScheme,
                        onClick = { onSchemeChange(scheme) }
                    )
                }
            }
        }
    }
}

@Composable
private fun ColorSchemeOption(
    scheme: AppColorScheme,
    isSelected: Boolean,
    onClick: () -> Unit
) {
    Column(
        horizontalAlignment = Alignment.CenterHorizontally,
        modifier = Modifier.clickable { onClick() }
    ) {
        // Ïª¥Îü¨ ÏÉòÌîå Ïõê
        Box(
            modifier = Modifier
                .size(60.dp)
                .clip(CircleShape)
                .background(scheme.primaryColor)
                .then(
                    if (isSelected) {
                        Modifier.padding(4.dp)
                    } else Modifier
                ),
            contentAlignment = Alignment.Center
        ) {
            if (isSelected) {
                Icon(
                    Icons.Default.Check,
                    contentDescription = "ÏÑ†ÌÉùÎê®",
                    tint = Color.White,
                    modifier = Modifier.size(24.dp)
                )
            }
        }
        
        Spacer(modifier = Modifier.height(8.dp))
        
        Text(
            text = scheme.displayName,
            fontSize = 12.sp,
            fontWeight = if (isSelected) FontWeight.Bold else FontWeight.Normal,
            color = if (isSelected) MaterialTheme.colorScheme.primary 
                   else MaterialTheme.colorScheme.onSurface
        )
        
        Text(
            text = scheme.description,
            fontSize = 10.sp,
            color = MaterialTheme.colorScheme.onSurfaceVariant,
            maxLines = 2,
            modifier = Modifier.width(80.dp)
        )
    }
}

