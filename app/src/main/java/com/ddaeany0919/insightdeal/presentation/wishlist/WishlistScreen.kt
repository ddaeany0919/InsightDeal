@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun WishlistScreen(
    viewModel: WishlistViewModel = viewModel()
) {
    var showAddDialog by remember { mutableStateOf(false) }
    val uiState by viewModel.uiState.collectAsState()
    
    // Ï¥àÍ∏∞ Îç∞Ïù¥ÌÑ∞ Î°úÎìú
    LaunchedEffect(Unit) {
        viewModel.loadWishlist()
    }
    
    Box(modifier = Modifier.fillMaxSize()) {
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(16.dp)
        ) {
            // Ìó§Îçî
            WishlistHeader(
                totalItems = uiState.wishlists.size,
                onAddClick = { showAddDialog = true }
            )
            
            Spacer(modifier = Modifier.height(16.dp))
            
            // ÏΩòÌÖêÏ∏†
            when {
                uiState.isLoading -> {
                    LoadingState()
                }
                uiState.wishlists.isEmpty() -> {
                    EmptyWishlistState { showAddDialog = true }
                }
                else -> {
                    WishlistContent(
                        wishlists = uiState.wishlists,
                        onItemClick = { /* TODO: ÏÉÅÏÑ∏ ÌôîÎ©¥ */ },
                        onDeleteClick = { viewModel.deleteWishlist(it.id) },
                        onPriceCheckClick = { viewModel.checkPrice(it.id) }
                    )
                }
            }
        }
        
        // Í¥ÄÏã¨ÏÉÅÌíà Ï∂îÍ∞Ä Îã§Ïù¥ÏñºÎ°úÍ∑∏
        if (showAddDialog) {
            AddWishlistDialog(
                onDismiss = { showAddDialog = false },
                onAdd = { keyword, targetPrice ->
                    viewModel.addWishlist(keyword, targetPrice)
                    showAddDialog = false
                }
            )
        }
        
        // ÏóêÎü¨ Ïä§ÎÇµÎ∞î
        uiState.errorMessage?.let { error ->
            LaunchedEffect(error) {
                // TODO: Ïä§ÎÇµÎ∞î ÌëúÏãú
                viewModel.clearError()
            }
        }
    }
}

