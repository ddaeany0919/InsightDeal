@Composable
private fun AddWishlistDialog(
    onDismiss: () -> Unit,
    onAdd: (String, Int) -> Unit
) {
    var keyword by remember { mutableStateOf("") }
    var targetPrice by remember { mutableStateOf("") }
    var keywordError by remember { mutableStateOf<String?>(null) }
    var priceError by remember { mutableStateOf<String?>(null) }
    
    Dialog(onDismissRequest = onDismiss) {
        Card(
            modifier = Modifier
                .fillMaxWidth()
                .padding(16.dp),
            shape = RoundedCornerShape(16.dp)
        ) {
            Column(
                modifier = Modifier.padding(24.dp)
            ) {
                Text(
                    text = "Í¥ÄÏã¨ÏÉÅÌíà Ï∂îÍ∞Ä",
                    style = MaterialTheme.typography.headlineSmall,
                    fontWeight = FontWeight.Bold
                )
                
                Spacer(modifier = Modifier.height(16.dp))
                
                // ÌÇ§ÏõåÎìú ÏûÖÎ†•
                OutlinedTextField(
                    value = keyword,
                    onValueChange = { 
                        keyword = it
                        keywordError = null
                    },
                    label = { Text("Í≤ÄÏÉâ ÌÇ§ÏõåÎìú") },
                    placeholder = { Text("Ïòà: ÏïÑÏù¥Ìè∞ 15, Í∞§Îü≠Ïãú S24") },
                    modifier = Modifier.fillMaxWidth(),
                    isError = keywordError != null,
                    supportingText = keywordError?.let { { Text(it) } }
                )
                
                Spacer(modifier = Modifier.height(12.dp))
                
                // Î™©Ìëú Í∞ÄÍ≤© ÏûÖÎ†•
                OutlinedTextField(
                    value = targetPrice,
                    onValueChange = { 
                        targetPrice = it.filter { char -> char.isDigit() }
                        priceError = null
                    },
                    label = { Text("Î™©Ìëú Í∞ÄÍ≤©") },
                    placeholder = { Text("1200000") },
                    keyboardOptions = KeyboardOptions(keyboardType = KeyboardType.Number),
                    modifier = Modifier.fillMaxWidth(),
                    isError = priceError != null,
                    supportingText = priceError?.let { { Text(it) } },
                    trailingIcon = {
                        Text(
                            text = "Ïõê",
                            color = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                    }
                )
                
                Spacer(modifier = Modifier.height(24.dp))
                
                // Î≤ÑÌäºÎì§
                Row(
                    modifier = Modifier.fillMaxWidth(),
                    horizontalArrangement = Arrangement.spacedBy(8.dp)
                ) {
                    OutlinedButton(
                        onClick = onDismiss,
                        modifier = Modifier.weight(1f)
                    ) {
                        Text("Ï∑®ÏÜå")
                    }
                    
                    Button(
                        onClick = {
                            // Ïú†Ìö®ÏÑ± Í≤ÄÏÇ¨
                            var hasError = false
                            
                            if (keyword.trim().length < 2) {
                                keywordError = "ÌÇ§ÏõåÎìúÎäî 2Í∏ÄÏûê Ïù¥ÏÉÅ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî"
                                hasError = true
                            }
                            
                            val price = targetPrice.toIntOrNull()
                            if (price == null || price <= 0) {
                                priceError = "Ïò¨Î∞îÎ•∏ Í∞ÄÍ≤©ÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî"
                                hasError = true
                            }
                            
                            if (!hasError) {
                                onAdd(keyword.trim(), price!!)
                            }
                        },
                        modifier = Modifier.weight(1f)
                    ) {
                        Text("Ï∂îÍ∞Ä")
                    }
                }
            }
        }
    }
}

